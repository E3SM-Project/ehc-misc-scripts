# elm2gcam_mapping_generator.py
#
# Python translation of the elm2gcam portion of e3sm_gcam_land_mapping.r
# (original R script by Alan Di Vittorio).
#
# PURPOSE:
#   Generates spatial mapping files that link each ELM (E3SM Land Model) grid
#   cell to one or more GCAM (Global Change Assessment Model) land units.
#   Each land unit is a region x GLU (Geographic Land Unit / basin) combination.
#   The output CSVs record what fraction ("Weight") of each ELM cell belongs to
#   each GCAM region+GLU, based on the MOIRAI high-resolution land-area raster.
#
# WORKFLOW:
#   Step 1 - Read base context data
#     - Parse woodharvest.xml to get the ordered list of GCAM land units
#       (region x basin combinations used by the IAC coupler).
#     - Read MOIRAI_reggcam_GLU_with_spaces.csv for region codes/names.
#     - Read basin_to_country_mapping.csv for GLU abbreviations and basin IDs.
#     - Build gcam_lu: a lookup table with out_reg_code, out_lu_code,
#       out_basin_code, gcam_reg_name, basin_abr, gcam_reg_code, gcam_basin_code.
#
#   Step 2 - Load high-resolution geospatial grids
#     - Open moirai_valid_region32_water_basin235.bsq: a 2160x4320 (5 arcminute)
#       raster where each pixel value = region_code * 10000 + GLU_code.
#     - Read hyde_cell_plus.bil and hyde_land_plus.bil for cell and land areas (km^2).
#     - Extrapolate missing cell-area values across each latitude band.
#
#   Step 3 - High-resolution US state boundary logic (computed but not used in output)
#     - Rasterize gcamusa_state_glu_wgs84.shp onto the MOIRAI grid.
#     - Apply a 7x7 focal-majority filter to fill USA region pixels without a
#       state assignment with the dominant neighboring state ID.
#     - Build moirai_state_rast (region x GLU code raster with state sub-regions
#       replacing the single USA region inside the contiguous US).
#     NOTE: moirai_state_rast is computed but not used in the output loop; the
#           _usa mapping files generated by the R script are not produced here.
#
#   Step 4 - Core mapping extraction logic (_pixel_fracs + process_elm_cell)
#     - For a single coarse ELM cell (identified by a flat cell index), compute
#       its lat/lon bounding box from the pre-calculated boundary arrays.
#     - Cells that straddle the dateline (e.g. the cell centred at 180 deg in
#       the f09/f19 grids) are handled as two horizontal strips that are
#       concatenated before aggregation.
#     - For each fine MOIRAI pixel in the window, _pixel_fracs() computes its
#       fractional overlap with the ELM cell polygon via an outer product of
#       1-D lon and lat overlap vectors, replicating R's
#       raster::extract(..., exact=TRUE) weighting.
#     - weighted_land_area = pixel_overlap_fraction x land_area for each pixel.
#     - Weight for each GCAM land unit = sum(weighted_land_area for LU) /
#       sum(weighted_land_area for all valid pixels in cell).
#     - Weights are normalised to 1 when some raster LU codes are absent from
#       gcam_lu (matches R: if(sumw < 1) weight = 1/sumw * weight).
#     - Results are merged with the gcam_lu lookup table to attach
#       region/GLU identifiers.
#
#   Step 5 - Loop through ELM grid resolutions
#     - Iterates over five ELM surface data files (0.9, 1.9, 0.5, 0.125, 0.25 deg),
#       reading LONGXY/LATIXY to derive cell boundaries.
#     - Calls process_elm_cell for every grid cell and concatenates results.
#     - Writes one CSV per resolution to ./mapping_outfiles/:
#         region_index, GLU_index, x (1-based lon index), y (1-based lat index),
#         gcam_reg_name, GLU_name, Weight
#
# INPUT FILES (all under /global/cfs/cdirs/e3sm/inputdata/iac/giac/inputs_for_mapping_generation/):
#   woodharvest.xml, MOIRAI_reggcam_GLU_with_spaces.csv,
#   basin_to_country_mapping.csv, moirai_valid_region32_water_basin235.bsq,
#   hyde_cell_plus.bil, hyde_land_plus.bil,
#   gcamusa_state_glu_wgs84/gcamusa_state_glu_wgs84.shp,
#   surfdata_0.9x1.25_HIST_simyr2015_c201021.nc,
#   surfdata_1.9x2.5_SSP5_RCP85_simyr2015_c210916.nc,
#   surfdata_0.5x0.5_HIST_simyr2015_c220318.nc,
#   surfdata_0.125x0.125_HIST_simyr2015_c241205.nc,
#   surfdata_0.25x0.25_simyr2015_c250312.nc
#
# OUTPUT FILES (written to ./mapping_outfiles/):
#   elm0.9x1.25togcam_mapping.csv, elm1.9x2.5togcam_mapping.csv,
#   elm0.5x0.5togcam_mapping.csv, elm0.125x0.125togcam_mapping.csv,
#   elm0.25x0.25togcam_mapping.csv

